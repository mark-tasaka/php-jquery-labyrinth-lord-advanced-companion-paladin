function undeadHD1 ($level)
{
    $turn = "X";
    
    if($level >= 1 && $level <=2)
    {
        $turn = "-";
    }
    else if($level == 3)
    {
        $turn = "5";
    }
    else if($level == 4)
    {
        $turn = "3";
    }
    else if($level >= 5 && $level <= 6)
    {
        $turn = "T";
    }
    else
    {
        $turn = "D";
    }
    
    return $turn;
}


